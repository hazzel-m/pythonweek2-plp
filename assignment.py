# Lists
# creating an emoty list
my_list = []
print(my_list)

# appending values to my_list
list = [10,20,30,40]
my_list.append(list)
print(my_list)

# Inserting a value to my_list
my_list[0].insert(1,15)
print(my_list)

# Extending the list
my_list[0].extend([50,60,70])
print(my_list)

# Removing the last value from my_list
my_list[0].pop()
print(my_list)

# Sorting in ascending order
my_list[0].sort()
print(my_list)

# finding and printing the index of 30
print(my_list[0].index(30))






